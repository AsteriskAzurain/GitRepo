package com.javaweb.po;
public class Product {
	//产品ID
	private String id;
	//产品名称
	private String name;
	//产品价格
	private double price;

	//各属性的setter和getter方法
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}



}
